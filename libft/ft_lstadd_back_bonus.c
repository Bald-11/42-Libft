#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*temp;

	if (*lst == NULL)
	{
		*lst = new;
		new->next = NULL;
		return ;
	}	
	temp = *lst;
	temp = ft_lstlast(temp);
	temp->next = new;
	new->next = NULL;
}
